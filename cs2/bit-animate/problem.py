def drawFrame(frame):
    output = "┌────────────────┐\n│"

    for index, hexChar in enumerate(frame):
        if index and index % 2 == 0:
            output += "│\n│"

        output += hexBlocks[hexChar]

    os.system("clear")
    output += "│\n└────────────────┘"
    print(output)


def animate(frames, fps):
    while True:
        for frame in frames:
            drawFrame(frame)
            time.sleep(1 / fps)


